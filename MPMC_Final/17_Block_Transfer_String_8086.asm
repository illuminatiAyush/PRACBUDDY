.MODEL SMALL
.STACK 100H

.DATA
    SOURCE DB 01H, 02H, 03H, 04H, 05H
    DEST   DB 5 DUP(?)

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV ES, AX
    MOV SI, OFFSET SOURCE
    MOV DI, OFFSET DEST
    MOV CX, 5
    CLD
    REP MOVSB
    MOV SI, OFFSET DEST
    MOV DI, OFFSET SOURCE
    MOV CX, 5
    CLD
    REP MOVSB
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
