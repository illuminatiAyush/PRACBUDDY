.MODEL SMALL
.STACK 100H

.DATA
    DIVIDEND DW 0064H
    DIVISOR  DB 0AH
    QUOTIENT DB ?
    REMAINDR DB ?

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV AX, DIVIDEND
    MOV BL, DIVISOR
    DIV BL
    MOV QUOTIENT, AL
    MOV REMAINDR, AH
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
