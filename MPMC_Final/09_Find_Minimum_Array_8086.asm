.MODEL SMALL
.STACK 100H

.DATA
    ARRAY DB 05H, 02H, 08H, 01H, 09H
    COUNT DB 05H
    MIN   DB ?

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV CL, COUNT
    MOV SI, 0
    MOV AL, ARRAY[SI]
    DEC CL
LP1:
    INC SI
    MOV BL, ARRAY[SI]
    CMP AL, BL
    JLE SKIP1
    MOV AL, BL
SKIP1:
    DEC CL
    JNZ LP1
    MOV MIN, AL
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
