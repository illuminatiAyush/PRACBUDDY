.MODEL SMALL
.STACK 100H

.DATA
    ARRAY DB 05H, 03H, 08H, 01H, 07H
    COUNT DB 05H

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV BL, COUNT
    DEC BL
OUTER:
    MOV CL, BL
    MOV SI, 0
INNER:
    MOV AL, ARRAY[SI]
    MOV DL, ARRAY[SI+1]
    CMP AL, DL
    JLE NOSWAP
    MOV ARRAY[SI], DL
    MOV ARRAY[SI+1], AL
NOSWAP:
    INC SI
    DEC CL
    JNZ INNER
    DEC BL
    JNZ OUTER
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
