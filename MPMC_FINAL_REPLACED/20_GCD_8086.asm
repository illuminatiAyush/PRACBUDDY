.MODEL SMALL
.STACK 100H

.DATA
    NUM1   DW 0048H
    NUM2   DW 0018H
    RESULT DW ?

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV AX, NUM1
    MOV BX, NUM2
GCD:
    CMP AX, BX
    JE DONE
    JL LESS
    SUB AX, BX
    JMP GCD
LESS:
    SUB BX, AX
    JMP GCD
DONE:
    MOV RESULT, AX
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
