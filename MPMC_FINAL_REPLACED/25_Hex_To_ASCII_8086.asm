.MODEL SMALL
.STACK 100H

.DATA
    HEXNUM DB 0FH
    ASCII  DB ?

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV AL, HEXNUM
    AND AL, 0FH
    CMP AL, 09H
    JLE NUM
    ADD AL, 37H
    JMP DONE
NUM:
    ADD AL, 30H
DONE:
    MOV ASCII, AL
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN
